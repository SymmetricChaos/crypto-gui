fn sbox(byte: u8) -> u8 {
    S_BOX[byte as usize]
}

fn sub_word(n: u32) -> u32 {
    u32::from_be_bytes(n.to_be_bytes().map(|b| sbox(b)))
}

fn sub_bytes(state: &mut [u8]) {
    for byte in state {
        *byte = sbox(*byte)
    }
}

fn mul2(byte: u8) -> u8 {
    MUL_2[byte as usize]
}

fn mul3(byte: u8) -> u8 {
    MUL_3[byte as usize]
}

