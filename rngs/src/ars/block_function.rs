fn next_key(key: &mut [u8]) {}

pub fn encrypt(bytes: &mut [u8], key: &mut [u8], rounds: usize) {
    transpose_state(bytes);
    // Initial round key
    add_key(bytes, key);

    // Main NR
    for _ in 1..rounds {
        sub_bytes(bytes);
        shift_rows(bytes);
        mix_columns(bytes);
        next_key(key);
        add_key(bytes, key);
    }

    // Finalization round
    sub_bytes(bytes);
    shift_rows(bytes);
    next_key(key);
    add_key(bytes, key);
    transpose_state(bytes);
}
